// Imagine que o seu irmão esteja aprendendo programação e o professor
// lhe solicitou um algoritmo que imprima os números de 1 a 10. Como 
// seria a implementação desse algoritmo para que ele possa apresentá-lo 
// ao seu professor?


function executarDesafio4() {
    alert("Veja o resultado no Console");
    let limNum = 10;
    for (let i = 1; i <= limNum; i++) {
        console.log(`${i}`);
    }
}
executarDesafio4();